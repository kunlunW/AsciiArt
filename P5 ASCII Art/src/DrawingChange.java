/**
 * This class serves to make changes to existing Ascii art canvas
 * 
 * @author Kunlun Wang, Mingi Kim
 *
 */
public class DrawingChange {
  public final int x; // x coordinate for a change
  public final int y; // y coordinate for a change
  public final char prevChar; // previous character in the (x,y)
  public final char newChar; // new character in the (x,y)

  /**
   * This is the constructor for this class. It serves to create a new change with given parameters
   * 
   * @param x represents x coordinate
   * @param y represents y coordinate
   * @param prevChar the previous char in the (x,y) in canvas
   * @param newChar represents the new char in the (x,y) in cnavas
   */
  public DrawingChange(int x, int y, char prevChar, char newChar) {
    this.x = x;
    this.y = y;
    this.prevChar = prevChar;
    this.newChar = newChar;
  }
}
